package com.example.dacn2.entity.hotel;

/**
 * Enum định nghĩa phong cách thiết kế của khách sạn
 * Sử dụng cho recommendation system để gợi ý theo sở thích design
 */
public enum DesignStyle {
    MODERN, // Hiện đại, tối giản
    TRADITIONAL, // Truyền thống, cổ điển
    BOUTIQUE, // Độc đáo, nghệ thuật
    MINIMALIST, // Tối giản, đơn giản
    LUXURY, // Xa hoa, sang trọng
    RUSTIC, // Mộc mạc, gần gụi thiên nhiên
    CONTEMPORARY // Đương đại, kết hợp nhiều phong cách
}
